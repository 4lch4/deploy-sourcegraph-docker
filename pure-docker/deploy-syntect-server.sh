    --name=syntect-server \
    --network=sourcegraph \
    --restart=always \
    --cpus=4 \
    --memory=6g \
    index.docker.io/sourcegraph/syntax-highlighter:3.35.2@sha256:8bcc3b9f9de36a8de0da4ab2ff409082e20bac054079f25cb4deeb632b225948

echo "Deployed syntect-server service"
